    private void jButtonChoixPhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChoixPhotoActionPerformed
        if (choixPhoto == null) {
            choixPhoto = new JFileChooser(".");
            choixPhoto.setFileFilter(new FileNameExtensionFilter("Images", "jpg", "png"));
        }
        choixPhoto.setCurrentDirectory(choixPhoto.getCurrentDirectory());
        choixPhoto.showOpenDialog(null);
        if (choixPhoto.getSelectedFile() == null) {
            choixPhoto = null;
        } else {
            try {
                this.lImage = new ImageIcon(choixPhoto.getSelectedFile().toString());
                
                jLabelPhoto.getGraphics().drawImage(this.lImage.getImage(),0,0, 94, 94, null);
                jLabelPhoto.setIcon(lImage);
                Cv_photo newPhoto = new Cv_photo();
                newPhoto.setEtudiant(this.etudiantPhoto);
                newPhoto.setPhoto(choixPhoto.getSelectedFile().toString());
                DaoS4.photoDao().createOrUpdate(newPhoto);
            } catch (SQLException ex) {
                Logger.getLogger(PanneauPhoto.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }

    }//GEN-LAST:event_jButtonChoixPhotoActionPerformed

